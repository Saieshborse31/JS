// while loop

let index = 0;
while (index <=10) {
    // console.log(`Index is ${index}`);
    index = index + 2;
}

let myArr = ["earth", "moon", "sun", "mars"];
let arr = 0;
while (arr < myArr.length) {
    // console.log(`${myArr[arr]}`);
    arr = arr + 1;
}


//do-while loop

let count = 1;
do {
    console.log(`Count is: ${count}`);
    count++;
} while (count <=10);